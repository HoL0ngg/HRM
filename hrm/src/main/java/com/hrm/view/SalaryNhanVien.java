    private void showSalaryChangeDialog(ActionEvent evt) {
        // Tạo dialog
        JDialog dialog = new JDialog(this, "Yêu cầu thay đổi lương", true);
        dialog.setSize(400, 250);
        dialog.setLayout(new GridLayout(4, 1, 10, 10));
        dialog.setLocationRelativeTo(this);

        // Tạo giao diện trong dialog
        JPanel panelSalary = createInputPanel("Mức lương đề xuất:", new JTextField(20));
        JPanel panelReason = createInputPanel("Lý do thay đổi:", new JTextField(20));
        JPanel panelButtons = createButtonPanel(dialog, panelSalary, panelReason);

        dialog.add(panelSalary);
        dialog.add(panelReason);
        dialog.add(panelButtons);

        dialog.setVisible(true);
    }

    /**
     * Tạo panel nhập liệu với nhãn và ô nhập
     */
    private JPanel createInputPanel(String labelText, JTextField textField) {
        JPanel panel = new JPanel(new FlowLayout(FlowLayout.LEFT));
        JLabel label = new JLabel(labelText);
        panel.add(label);
        panel.add(textField);
        return panel;
    }

