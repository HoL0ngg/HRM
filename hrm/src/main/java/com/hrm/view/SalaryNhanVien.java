    private void loaddata() {
        this.txtHoTen.setText(employee.getName());
        SalaryDAO salaryDao = new SalaryDAO();
        Salary salary = salaryDao.selectByEmployeeID(employee.getId());
        EmployeeDAO employeeDao = new EmployeeDAO();
        Employee employee1 = employeeDao.selectByID(employee.getId());
        this.txtViTri.setText(employee1.getPosition().getName());
        this.txtTongLuongTheoGio.setText(salary.getHourly_salary() + "");
        this.txtTongLuongTangCa.setText(salary.getOvertime_hourly_salary() + "");
        this.txtChuyenCan.setText(salary.getAttendance() + "");
        this.txtThuong.setText(salary.getBonus() + "");
        this.txtKhauTru.setText(salary.getDeductions() + "");
        this.txtTongLuong.setText(salary.getnet_salary() + "");
        this.txtNgayHieuLuc.setText(salary.getPayday() + "");
        this.txtGhiChu.setText(salary.getNote() + " ");
    }

